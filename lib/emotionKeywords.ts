// 감정에 매핑된 키워드 풀에서 하나를 무작위로 고른다. (없으면 null)
export function pickKeyword(
  emotion: string,
  rand: () => number = Math.random
): string | null {
  const pool = emotionKeywords[emotion]
  if (!pool || pool.length === 0) return null
  return pool[Math.floor(rand() * pool.length)]
}
